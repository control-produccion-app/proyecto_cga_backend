CREATE TABLE jornada_diaria (
    id_jornada BIGINT GENERATED BY DEFAULT AS IDENTITY,
    fecha DATE NOT NULL,
    CONSTRAINT pk_jornada_diaria PRIMARY KEY (id_jornada)
);

CREATE TABLE turno (
    id_turno SMALLINT GENERATED BY DEFAULT AS IDENTITY,
    nombre_turno VARCHAR(20) NOT NULL,
    CONSTRAINT pk_turno PRIMARY KEY (id_turno),
    CONSTRAINT uq_turno_nombre UNIQUE (nombre_turno)
);

CREATE TABLE distribucion (
    id_distribucion BIGINT GENERATED BY DEFAULT AS IDENTITY,
    nombre_distribucion VARCHAR(50) NOT NULL,
    CONSTRAINT pk_distribucion PRIMARY KEY (id_distribucion),
    CONSTRAINT uq_distribucion_nombre UNIQUE (nombre_distribucion)
);

CREATE TABLE insumo (
    id_insumo BIGINT GENERATED BY DEFAULT AS IDENTITY,
    nombre_insumo VARCHAR(100) NOT NULL,
    unidad_control VARCHAR(20) NOT NULL,
    stock_sugerido_inicial NUMERIC(10,2),
    activo CHAR(1),
    CONSTRAINT pk_insumo PRIMARY KEY (id_insumo),
    CONSTRAINT chk_insumo_activo CHECK (activo IS NULL OR activo IN ('S','N'))
);

CREATE TABLE tipo_produccion (
    id_tipo_produccion BIGINT GENERATED BY DEFAULT AS IDENTITY,
    nombre_tipo_produccion VARCHAR(150) NOT NULL,
    id_insumo_principal BIGINT NOT NULL,
    CONSTRAINT pk_tipo_produccion PRIMARY KEY (id_tipo_produccion),
    CONSTRAINT fk_tp_insumo FOREIGN KEY (id_insumo_principal)
        REFERENCES insumo (id_insumo)
);

CREATE TABLE producto (
    id_producto BIGINT GENERATED BY DEFAULT AS IDENTITY,
    nombre_producto VARCHAR(150) NOT NULL,
    precio_sugerido NUMERIC(10,2) NOT NULL,
    unidad_venta_base VARCHAR(10) NOT NULL,
    id_tipo_produccion BIGINT NOT NULL,
    CONSTRAINT pk_producto PRIMARY KEY (id_producto),
    CONSTRAINT fk_prod_tipo FOREIGN KEY (id_tipo_produccion)
        REFERENCES tipo_produccion (id_tipo_produccion),
    CONSTRAINT chk_producto_unidad_venta_base CHECK (
        unidad_venta_base IN ('KILO','UNIDAD','AMBOS')
    )
);

CREATE TABLE cliente (
    id_cliente BIGINT GENERATED BY DEFAULT AS IDENTITY,
    rut INTEGER NOT NULL,
    digito_verificador CHAR(1) NOT NULL,
    nombre_cliente VARCHAR(150) NOT NULL,
    ciudad VARCHAR(100) NOT NULL,
    direccion VARCHAR(200) NOT NULL,
    telefono VARCHAR(20),
    descuento_aplicado NUMERIC(5,2),
    CONSTRAINT pk_cliente PRIMARY KEY (id_cliente),
    CONSTRAINT uq_cliente_rut UNIQUE (rut, digito_verificador),
    CONSTRAINT chk_cliente_descuento CHECK (
        descuento_aplicado IS NULL
